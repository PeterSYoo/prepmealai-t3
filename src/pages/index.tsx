import { type NextPage } from "next";
import Head from "next/head";
import { AuthProviders } from "~/components/Root/AuthProviders.components";
import { LoginForm } from "~/components/Root/Login/LoginForm.components";
import { useState } from "react";
import { SignupForm } from "~/components/Root/Signup/SignupForm.components";
import { ErrorModal } from "~/components/Root/ErrorModal.components";
import { SuccessModal } from "~/components/Root/SuccessModal.components";

const Home: NextPage = () => {
  const [isLogin, setIsLogin] = useState<boolean>(true);
  const [isErrorModal, setIsErrorModal] = useState<boolean>(false);
  const [isSuccessModal, setIsSuccessModal] = useState<boolean>(false);

  return (
    <>
      {isErrorModal && <ErrorModal setIsErrorModal={setIsErrorModal} />}
      {isSuccessModal && (
        <SuccessModal
          setIsSuccessModal={setIsSuccessModal}
          setIsLogin={setIsLogin}
        />
      )}
      <Head>
        <title>PropMeal AI</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link
          rel="apple-touch-icon"
          sizes="180x180"
          href="/apple-touch-icon.png"
        />
        <link
          rel="icon"
          type="image/png"
          sizes="32x32"
          href="/favicon-32x32.png"
        />
        <link
          rel="icon"
          type="image/png"
          sizes="16x16"
          href="/favicon-16x16.png"
        />
        <link rel="manifest" href="/site.webmanifest" />
        <meta name="msapplication-TileColor" content="#da532c" />
        <meta name="theme-color" content="#ffffff" />
      </Head>
      <main className="flex min-h-screen flex-col items-center justify-center gap-6 bg-gradient-to-b from-[#abd1d9] to-[#ffffff]">
        {/* Login Form */}
        {isLogin && (
          <LoginForm
            setIsLogin={setIsLogin}
            setIsErrorModal={setIsErrorModal}
          />
        )}
        {/*  */}
        {/* Signup Form */}
        {!isLogin && (
          <SignupForm
            setIsLogin={setIsLogin}
            setIsSuccessModal={setIsSuccessModal}
          />
        )}
        {/*  */}
        <div className="grid w-full max-w-[400px] grid-cols-[1fr_50px_1fr]">
          <div className="mb-2.5 border-b border-black"></div>
          <p className="text-center">or</p>
          <div className="mb-2.5 border-b border-black"></div>
        </div>
        <AuthProviders />
        <button
          onClick={() => setIsErrorModal(true)}
          className="border border-black px-2 py-1"
        >
          Open Modal
        </button>
      </main>
    </>
  );
};

export default Home;
